package loop_Study;

public class Do_while_loop {

	public static void main(String[] args) {
	//to print 1 to 20 no 
		int i=1;
		do 
		{
			System.out.println(i);
			i++;
		}
		while(i<=20);

		
		
		//to print Pune 10times

		String city="Pune";

		int u=1;
		do
		{
			System.out.println(city);
			u++;
		}
		while(u<=10);
	
//to print 6 table in reverse order
		
		int o=60;
		do 
		{
			System.out.println(o);
			o=o-6;
		}
		while(o>=6);
	}
	

}
