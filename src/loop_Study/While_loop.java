package loop_Study;

public class While_loop {

	public static void main(String[] args) {
		//table of 3
		int i=3;
		while (i<=30) {
			System.out.println(i);
		i=i+3;
		
		}
		
		String name ="Pune";
		int j=1;
		while(j<=10) {
			System.out.println(name);
			j++;
		}
		
		//print 11 table in reverse order
		int h=110;
		while (h>=11) {
			System.out.println(h);
		h=h-11;
		}
		}

	}


